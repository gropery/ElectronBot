namespace UIWidgets
{
	/// <summary>
	/// Paginator direction.
	/// </summary>
	public enum PaginatorDirection
	{
		/// <summary>
		/// Auto detect direction using ScrollRect.Direction and size of ScrollRect.Content.
		/// </summary>
		Auto = 0,

		/// <summary>
		/// Horizontal.
		/// </summary>
		Horizontal = 1,

		/// <summary>
		/// Vertical.
		/// </summary>
		Vertical = 2,
	}
}