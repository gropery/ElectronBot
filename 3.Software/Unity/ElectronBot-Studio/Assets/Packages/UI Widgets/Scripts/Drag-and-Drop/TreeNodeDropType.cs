namespace UIWidgets
{
	/// <summary>
	/// TreeNode drop type.
	/// </summary>
	public enum TreeNodeDropType
	{
		/// <summary>
		/// Drop as child of current node.
		/// </summary>
		Child = 0,

		/// <summary>
		/// Drop before current node.
		/// </summary>
		Before = 1,

		/// <summary>
		/// Drop after current node.
		/// </summary>
		After = 2,
	}
}